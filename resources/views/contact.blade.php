                            $(document).ready(function() {
                                $('#contactForm').on('submit', function(e) {
                                    e.preventDefault(); // Prevent default form submission

                                    // Clear previous error messages
                                    $('.text-danger').text('');

                                    $.ajax({
                                        type: 'POST',
                                        url: '/contact/submit_contact',
                                        data: $(this).serialize(),
                                        success: function(response) {
                                            // Show success message using SweetAlert2
                                            Swal.fire({
                                                icon: 'success',
                                                title: 'Success!',
                                                text: response.success,
                                                showConfirmButton: true
                                            });

                                            $('#contactForm')[0].reset(); // Reset the form
                                        },
                                        error: function(xhr) {
                                            let errors = xhr.responseJSON.errors;

                                            // Iterate through errors and display them in the corresponding input field
                                            $.each(errors, function(key, value) {
                                                $('#' + key + 'Error').text(value[0]); // Display first error message
                                            });

                                            // Optionally, you can show an error message using SweetAlert2 here
                                            Swal.fire({
                                                icon: 'error',
                                                title: 'Oops...',
                                                text: 'There were some errors with your submission.Please Solve it',
